from telegram import Update
from telegram.ext import CallbackContext
from modern_bot.handlers.common import safe_reply
from modern_bot.handlers.admin import is_admin

async def help_handler(update: Update, context: CallbackContext) -> None:
    user_id = update.effective_user.id
    
    if is_admin(user_id):
        help_text = (
            "🛠 <b>Справочник администратора</b>\n\n"
            "<b>Основные кнопки</b>\n"
            "• <b>📝 Новое заключение</b> — запуск формы\n"
            "• <b>⚙️ Админ-панель</b> — все инструменты управления\n"
            "• <b>🏆 Мой рейтинг</b> — личная статистика\n"
            "• <b>ℹ️ Помощь</b> — эта подсказка\n\n"
            "<b>Инструменты панели</b>\n"
            "• <b>📊 Статистика</b> и <b>📈 Аналитика</b> — отчеты по периодам\n"
            "• <b>🔎 Поиск</b> — найти заключение по номеру билета\n"
            "• <b>📦 Архив</b> — выгрузка документов (месяц/регион)\n"
            "• <b>📋 История</b> — последние записи\n"
            "• <b>👥 Пользователи</b> и <b>⚙️ Администраторы</b> — доступы\n"
            "• <b>📢 Рассылка</b> — сообщения пользователям\n"
            "• <b>🖥️ Система</b> — БД и обслуживание\n\n"
            "<b>Повторные заключения</b>\n"
            "Если билет уже был обработан, заключение создается повторно, "
            "но <b>баллы не начисляются</b>.\n\n"
            "<b>🔍 Сверка билетов</b>\n"
            "Загрузка файла со списком билетов для проверки.\n"
            "<b>Формат:</b> <b>.xlsx</b> или <b>.txt</b>, номера — в первом столбце."
        )
    else:
        help_text = (
            "🤖 <b>Справка по боту</b>\n\n"
            "<b>Как создать заключение:</b>\n"
            "1. Нажмите <b>📝 Новое заключение</b>\n"
            "2. Заполните данные (подразделение, № заключения, № билета, дата, регион)\n"
            "3. Добавьте предметы с фото\n"
            "4. Нажмите <b>ПРОВЕРИТЬ И ОТПРАВИТЬ</b>\n"
            "5. Получите готовый DOCX\n\n"
            "<b>Повторные заключения</b>\n"
            "Если билет уже был обработан, заключение создается повторно, "
            "но <b>баллы не начисляются</b>.\n\n"
            "<b>ℹ️ Помощь:</b>\n"
            "Если возникли вопросы — обратитесь к администратору."
        )
    
    await safe_reply(update, help_text, parse_mode="HTML")
